    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:

  String MaSV = txtma.getText();
    Grade student = sqlasm.findById(MaSV);
    
    if (student != null) {
        lbName.setText(student.getHoTen());
        txtma2.setText(student.getMaSV());
        txtTiengAnh.setText(String.valueOf(student.getTiengAnh()));
        txtTin.setText(String.valueOf(student.getTinHoc()));
        txtGDTC.setText(String.valueOf(student.getGdtc()));
        lblDiemTB.setText(String.format("%.2f", student.getDiemTB()));
    } else {
        JOptionPane.showMessageDialog(this, "Không tìm thấy sinh viên có mã: " + MaSV);
    }
    }//GEN-LAST:event_btnSearchActionPerformed

