    public Grade getForm() {
        Grade gr = new Grade();
        gr.setHoTen(txtma.getText());
        gr.setMaSV(txtma2.getText());
        gr.setTiengAnh(Double.valueOf(txtTiengAnh.getText()));
        gr.setTinHoc(Double.valueOf(txtTin.getText()));
        gr.setGdtc(Double.valueOf(txtGDTC.getText()));
        return gr;
    }

    public void init() {

        listDiem = sqlasm.getAllGrade();
        String header[] = {"Mã sinh viên", "Họ tên", "Tiếng anh", "Tin học", "GDTC", "Điểm TB"};
        DefaultTableModel tblModel = new DefaultTableModel(header, 0);

        for (Grade sp : listDiem) {
            tblModel.addRow(sp.toRow());
        }

        tblList.setModel(tblModel);
    }

    public void fillTable() {

        Comparator comp = new Comparator<Grade>() {
            @Override
            public int compare(Grade o1, Grade o2) {
                if (o1.getDiemTB() < o2.getDiemTB()) {
                    return 1;

                } else {
                    return -1;
                }
            }
        };
        Collections.sort(listDiem, comp);
        defaultTableModel.setRowCount(0);
        for (int i = 0; i < 3; i++) {
            defaultTableModel.addRow(listDiem.get(i).toRow());

        }
    }

